-- Opciones básicas de Neovim

vim.opt.number = true         -- Muestra el número de línea
vim.opt.relativenumber = true -- Números relativos para moverse rápido
vim.opt.tabstop = 2           -- Un tab equivale a 4 espacios
vim.opt.shiftwidth = 2        -- Indentación de 4 espacios
vim.opt.expandtab = true      -- Convierte tabs en espacios
vim.opt.termguicolors = true  -- Habilita colores verdaderos en la terminal
vim.opt.mouse = 'a'           -- Habilita el uso del mouse en todos los modos
